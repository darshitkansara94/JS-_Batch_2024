// DataType :
//     Convert a value into a particular format / type.

//     Datatype in JS :
//         var:
//             var is used in older version of JS.
//             Microsoft recommanded to use var not much.
//         let :
//             let is laternate of var.
//             To replace var, let is introduced.
//             Better memory management.
//             Once variable is out of scope memory is released for variable.
//         const :
//             When your value is static that time you can use const.
//             We can not change the value of const var at the runtime.


// Variable :
//     Variable is a identification of value.
//     variable name can not start with special char.
//     variable name should be in camel case.
//     Space is not allowed in variable name.


// Example :
    // Declaration
    var cde;

    // Intilization
    cde = "WelTech1";

    // Declare with Intilization
    var abc = "WelTech";

    var intValue = 10;

    // Declare and Intilization with blank value.
    var cde = "";

